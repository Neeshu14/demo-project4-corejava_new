package variable_project;

public class IncrementDecrement {
    public static void main(String[] args) {
        // This is Increment Operator
        int n= 98;
        n++;

        System.out.println("Increment Operator is: "+n);

        // This is Decrement operator
        double q=12.5;
        q--;
        System.out.println("This is Decrement Operator: "+q);



    }
}
