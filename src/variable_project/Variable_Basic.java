package variable_project;

public class Variable_Basic {

    public static void main(String[] args) {
        // This is  Example of Basic Variable in JAVA
        // Syntax DataType varName;
        String name; // created variable with String Datatype
        int age; // to Store Age of as person
        char section; // to store section
        long phone;
        double weight;

        // varName
        name ="Neeshu Bhadauriya";
        age = 29;
        section = 'B';
        phone = 8076898;
        weight= 72.57;


        System.out.println( "Name of the Student :" +name);
        System.out.println( "Age :" +age);
        System.out.println( "Class Section :" +section);
        System.out.println( "Phone: " +phone);
        System.out.println( "Weight: " +weight);





    }
}
