package variable_test;

public class IncrementDecrement {
    public static void main(String[] args) {
        //Increment operator of x(int) and y (double)
        int x=56;
        double y= 66.72;
        x++;
        System.out.println("Increment of x: "+x);
        y++;
        System.out.println("Increment of y: "+y);

        //Decrement operator of same
        x--;
        System.out.println("Decrement of x: "+x);
        y--;
        System.out.println("Decrement of y: "+y);



    }
}
